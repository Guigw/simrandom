<?php

namespace Yrial\Simrandom\Domain\ValueObject;

class Randomizer
{
    public readonly string $name;

    /**
     * @param string $name
     */
    public function __construct(string $name)
    {
        $this->name = $name;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }
}